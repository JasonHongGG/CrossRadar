def test_apply_station_uk_values_updates_all_stations_with_primary_reference() -> None:
    payload = {
        "metadata": {"note": "old"},
        "stations": [
            {"stationCode": "1210", "stationName": "新竹"},
            {"stationCode": "3140", "stationName": "造橋"},
            {"stationCode": "3430", "stationName": "二水"},
        ],
    }

    updated_payload, missing_codes = module.apply_station_uk_values(
        payload,
        {
            "1210": ["縱貫線 K105 + 800", "內灣線 0K + 000", "六家線 0K + 000"],
            "3140": ["山線 K128 + 800"],
            "3430": ["縱貫線 K243 + 300", "集集線 0K + 000"],
        },
    )

    stations = updated_payload["stations"]
    assert stations[0]["UK"] == ["縱貫線 K105 + 800", "內灣線 0K + 000", "六家線 0K + 000"]
    assert stations[0]["UK_primary"] == "縱貫線 K105 + 800"
    assert stations[1]["UK"] == ["山線 K128 + 800"]
    assert stations[1]["UK_primary"] == "山線 K128 + 800"
    assert stations[2]["UK"] == ["縱貫線 K243 + 300", "集集線 0K + 000"]
    assert stations[2]["UK_primary"] == "縱貫線 K243 + 300"
    assert missing_codes == []
    assert "For compatibility, `UK` remains an ordered list" in updated_payload["metadata"]["note"]


